import Foundation

protocol FunctionsPattern2Protocol {
    func func1() -> String
    func func2(arg: String)
    func func3(arg: Int) -> String
    func func4(_ arg: Int) -> String
    func func5(for arg1: String, arg2: Int) -> Int
}

